// This is the main function that calculates the sum of three numbers
export default function getSumOfHoods(
    initialNumber, // The initial number
    expansion1989 = 89, // The number representing the expansion in 1989, default value is 89
    expansion2019 = 19, // The number representing the expansion in 2019, default value is 19
  ) {
    // Return the sum of the three numbers
    return initialNumber + expansion1989 + expansion2019;
}